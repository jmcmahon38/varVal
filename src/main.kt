



fun main(args: Array<String>) {


    var myBit: Byte = 8
    var myShort: Short = 300
    var myInt: Int = 10
    var myLong = 40L
    var myFloat: Float = 3.14f
    var myDouble: Double = 1.23456789
    var myBool: Boolean = true
    var myChar: Char = '\$'

    println("bit: $myBit, \n" +
            "short: $myShort \n" +
            "int: $myInt, \n" +
            "long: $myLong, \n" +
            "float: $myFloat, \n" +
            "double: $myDouble, \n" +
            "bool: $myBool, \n" +
            "char: $myChar")
}