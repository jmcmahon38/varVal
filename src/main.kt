



fun main(args: Array<String>) {

    val myBit: Byte = 8
    val myShort: Short = 300
    val myInt: Int = 10
    val myLong = 40L
    val myFloat: Float = 3.14f
    val myDouble: Double = 1.23456789
    val myBool: Boolean = true
    val myChar: Char = '\$'

    println("bit: $myBit \n" +
            "short: $myShort \n" +
            "int: $myInt \n" +
            "long: $myLong \n" +
            "float: $myFloat \n" +
            "double: $myDouble \n" +
            "bool: $myBool \n" +
            "char: $myChar")
}